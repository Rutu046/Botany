        <h4 class="heading4">Peer Review Process</h4>
        <p>
            Publication Process for BotScanPapers
            Receive a paper from potential author. Record the date the paper was received and give the paper a unique code
            number. Within Two Weeks send a letter to the author indicating that the paper has arrived and we are
            processing it. Send the paper to the referees for review. Record the date the paper was sent for review.
            Review should not take longer than Two Months. If the paper has not been reviewed in this time, contact the
            reviewer. After the paper has been reviewed show the result to the Editor or Manager. Return the paper with
            comments to the author. Ask that requested changes are done quickly (Two Weeks) and that the author send an
            updated document file. Send the paper to the Editor for English review. Once the paper has been returned (Two
            Weeks), make the corrections and typeset the paper as an x.doc file. Send the x.doc file to the Associate
            Editors, They so that they can check the paper follows the rules set down in the BotScans Notes for Authors.
            This should require a Few Days per paper. Once youâ€™ve received the papers from the Associate Editors convert
            the x.doc file into a pdf file. Send the pdf file and the standard e-mail to the authors for final
            corrections. When you receive their comments make changes, if necessary, to the x.doc file in preparation for
            printing. The author is given Two Weeks to submit changes. When page numbers are known prepare a pdf file of
            the paper for publication on the BotScan home page
        </p>
